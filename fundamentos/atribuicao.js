//Existe operador de atribuição e algumas variações de operador de atribuição, usando algumas operações aritiméticas

const a = 7;
let b = 3;

b += a; //atribuição aditiva, junto com a atribuição estou fazendo uma adição;  O mesmo que = b = b + a;
console.log(b);

b -= 4 // b = b - 4;
console.log(b);

b *= 2; //b = b * 2;
console.log(b);

b /= 2; //b = b / 2;
console.log(b);

b %= 2; // b = b % 2;
console.log(b);


